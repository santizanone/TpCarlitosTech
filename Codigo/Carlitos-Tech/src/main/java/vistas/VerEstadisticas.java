     private void cargarDatosTabla() {
        Dao<Venta> dao = new VentaDao();
        try {
            ventas = dao.getList();
        } catch (SQLException ex) {
            Logger.getLogger(VerInventario.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultTableModel modelo = (DefaultTableModel) TablaDatos.getModel();
        if(ventas != null){
            for (Venta p : ventas) {
                Vector<Object> vector = new Vector<>();
                vector.add(p.getFechaHora());
                vector.add(p.getPrecio());                 
                modelo.addRow(vector);
            }
        }

    }
    
    
    
     private void añadirListener(){
         TablaDatos.addMouseListener(new MouseAdapter() {
             @Override
             public void mouseClicked(MouseEvent e) {
                 int fila = TablaDatos.getSelectedRow();                 
                 if(fila != -1 && e.getClickCount() == 2 && SwingUtilities.isLeftMouseButton(e)){
                     JFrame ventana = new VerProductosEstadistica(ventas.get(fila));
                     ventana.setVisible(true);
                 }
             }
             
         });
     }
    
    
    
    
    
    
    
    
